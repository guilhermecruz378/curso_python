# Tipo de dado bool (boolean)
# Ao questionar algo em um programa,
# só existe duas resposatas possiveis:
# sim (True) ou não (False).
# Existe varios poperadores para "questionar"
# dentre eles o ==, que é o operador lógico que 
# que questiona se u mvalor é igual a outro
print(10 == 10) # sim -> True (verdadeiro)
print(10 == 11) # não -> False (Falso)
print( type(True))
print( type(False))
print(type(10 == 10))
print(type(10 == 11))