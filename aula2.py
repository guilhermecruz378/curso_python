# \r\n ou \n é usado para quebra de linha  \r\n -> CRLF 
# \n -> LF

print(12, 34, sep=' ') #função 'print' exibe as coisas na tela, e eu coloco argumentos dentro dos ()
print(54, 34, sep="-")  # sep= se chama separador ele separa os argumentos com oque está dentro das aspas
print(65, 22, end='$') # end='' significa final da linha, ele faz com que não aconteça a quebra de linha
print(120, 43)