primeiro_valor = input('Digite um valor: ')
segundo_valor = input('Digite o segundo valor: ')

if primeiro_valor > segundo_valor:
    print(
        f'O primeiro_valor {primeiro_valor}'
          f'é maior do que o segundo_valor {segundo_valor}!'
          )
else:
    print(
        f'O segundo_valor {segundo_valor}'
          f' é maior do que o primeiro_valor {primeiro_valor}!'
          )

